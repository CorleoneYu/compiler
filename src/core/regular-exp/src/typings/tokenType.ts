enum TokenType {
  EOF = 'eof', // 结尾
  NORMAL = 'normal', // 普通字符
  '.' = '.', // 通配符 .
  '^' = '^', // ^ 开头匹配符
  '$' = '$', // $ 末尾匹配符
  ']' = ']', // 字符集结尾括号 ]
  '[' = '[', // 字符集开始括号 [
  '{' = '{', // 限定符集开始括号 {
  '}' = '}', // 限定符集结尾括号 }
  '*' = '*', // 限定符 *
  '?' = '?', // 限定符 ?
  '+' = '+', // 限定符 +
  '(' = '(', // 子表达式开始括号 (
  ')' = ')', // 子表达式结束括号 )
  '-' = '-', // 连词符
  '|' = '|', // 或
}

export default TokenType;
